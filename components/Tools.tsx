const WeatherTool = () => {
  const [search, setSearch] = useState('');
  const [debouncedSearch, setDebouncedSearch] = useState('');
  const [data, setData] = useState<{name: string, temp: number, condition: string, humidity: number, wind: number} | null>(null);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState('');

  // Initial load
  useEffect(() => {
    fetchWeather('Kathmandu');
  }, []);

  useEffect(() => {
    const timer = setTimeout(() => {
        if(search) setDebouncedSearch(search);
    }, 800);
    return () => clearTimeout(timer);
  }, [search]);

  useEffect(() => {
    if (debouncedSearch) fetchWeather(debouncedSearch);
  }, [debouncedSearch]);

  const fetchWeather = async (city: string) => {
    setLoading(true);
    setError('');
    try {
      // 1. Get Lat/Lon from Open-Meteo Geocoding
      const geoResponse = await fetch(`https://geocoding-api.open-meteo.com/v1/search?name=${encodeURIComponent(city)}&count=1&language=en&format=json`);
      const geoData = await geoResponse.json();

      if (!geoData.results || geoData.results.length === 0) {
        throw new Error('City not found');
      }

      const { latitude, longitude, name } = geoData.results[0];

      // 2. Get Weather Data
      const weatherResponse = await fetch(`https://api.open-meteo.com/v1/forecast?latitude=${latitude}&longitude=${longitude}&current=temperature_2m,relative_humidity_2m,weather_code,wind_speed_10m`);
      const weatherData = await weatherResponse.json();

      const { temperature_2m, relative_humidity_2m, weather_code, wind_speed_10m } = weatherData.current;

      setData({
        name: name,
        temp: Math.round(temperature_2m),
        condition: getWeatherCondition(weather_code),
        humidity: relative_humidity_2m,
        wind: wind_speed_10m
      });
    } catch (err) {
      setError('Could not fetch weather. Please try again.');
    } finally {
      setLoading(false);
    }
  };

  const getWeatherCondition = (code: number) => {
    // WMO Weather interpretation codes (WW)
    if (code === 0) return 'Clear Sky';
    if (code >= 1 && code <= 3) return 'Partly Cloudy';
    if (code >= 45 && code <= 48) return 'Foggy';
    if (code >= 51 && code <= 67) return 'Rainy';
    if (code >= 71 && code <= 77) return 'Snowy';
    if (code >= 80 && code <= 82) return 'Rain Showers';
    if (code >= 95 && code <= 99) return 'Thunderstorm';
    return 'Cloudy';
  };

  const getIcon = (condition: string) => {
    if (condition === 'Clear Sky') return Sun;
    if (condition === 'Rainy' || condition === 'Rain Showers' || condition === 'Thunderstorm') return CloudRain;
    if (condition === 'Snowy') return CloudSnow;
    return Cloud;
  };

  const Icon = data ? getIcon(data.condition) : Cloud;

  return (
    <div className="flex flex-col h-full gap-6">
       <div className="flex justify-between items-center">
         <h3 className="text-2xl font-bold text-gold">Global Weather</h3>
         <span className="text-[10px] text-gray-500">Powered by Open-Meteo</span>
       </div>
       
       <div className="flex gap-2">
         <div className="relative flex-1">
            <Search className="absolute left-3 top-1/2 -translate-y-1/2 text-gray-400" size={18} />
            <input 
                type="text" 
                value={search}
                onChange={(e) => setSearch(e.target.value)}
                placeholder="Enter City (e.g., Kathmandu, London)"
                className="w-full bg-black/40 border border-white/10 rounded-lg pl-10 pr-4 py-3 text-white focus:border-gold outline-none"
            />
         </div>
       </div>

       <div className="flex-1 flex flex-col items-center justify-center text-center p-6 bg-gradient-to-b from-blue-500/20 to-transparent rounded-2xl border border-white/10 animate-in fade-in duration-500 relative">
          {loading ? (
             <div className="absolute inset-0 flex items-center justify-center bg-black/20 backdrop-blur-sm rounded-2xl z-10">
               <RefreshCw className="animate-spin text-gold w-8 h-8" />
             </div>
          ) : null}

          {error ? (
            <div className="text-red-400 flex flex-col items-center gap-2">
              <AlertCircle size={40} />
              <p>{error}</p>
            </div>
          ) : data ? (
            <>
              <Icon size={80} className="text-white mb-4 animate-pulse" />
              <h2 className="text-6xl font-thin mb-2">{data.temp}Â°<span className="text-3xl">C</span></h2>
              <p className="text-2xl font-bold text-gold mb-1">{data.name}</p>
              <p className="text-lg text-blue-200">{data.condition}</p>
              
              <div className="mt-8 grid grid-cols-3 gap-4 w-full">
                <div className="bg-black/20 p-2 rounded border border-white/5">
                  <span className="block text-xs text-gray-400">Humidity</span>
                  <span className="font-bold">{data.humidity}%</span>
                </div>
                <div className="bg-black/20 p-2 rounded border border-white/5">
                  <span className="block text-xs text-gray-400">Wind</span>
                  <span className="font-bold">{data.wind} km/h</span>
                </div>
                <div className="bg-black/20 p-2 rounded border border-white/5">
                  <span className="block text-xs text-gray-400">UV</span>
                  <span className="font-bold">Mod</span>
                </div>
              </div>
            </>
          ) : (
            <div className="text-gray-500">Search for a city to see weather data</div>
          )}
       </div>
    </div>
  );
};

