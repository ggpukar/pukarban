const Converter = () => {
  const [file, setFile] = useState<File | null>(null);
  const [targetFormat, setTargetFormat] = useState('PDF');
  const [status, setStatus] = useState<'idle' | 'converting' | 'done'>('idle');

  const handleFileChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    if (e.target.files && e.target.files[0]) {
      setFile(e.target.files[0]);
      setStatus('idle');
    }
  };

  const downloadFile = () => {
    if (!file) return;
    
    // Create a dummy content for the converted file
    const content = `This is a converted version of ${file.name}.\nTarget Format: ${targetFormat}\n\n(This is a demonstration file generated by the portfolio site)`;
    const blob = new Blob([content], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    
    const a = document.createElement('a');
    a.href = url;
    // Remove extension and append new one
    const nameWithoutExt = file.name.substring(0, file.name.lastIndexOf('.')) || file.name;
    a.download = `${nameWithoutExt}_converted.${targetFormat.toLowerCase()}`;
    document.body.appendChild(a);
    a.click();
    
    // Cleanup
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  };

  const convert = () => {
    if (!file) return;
    setStatus('converting');
    setTimeout(() => {
      setStatus('done');
    }, 2000); 
  };

  return (
    <div className="flex flex-col gap-6 h-full justify-center">
      <div className="flex justify-between items-center mb-2">
        <h3 className="text-2xl font-bold text-gold">Universal Doc Converter</h3>
        <span className="text-xs bg-white/10 px-2 py-1 rounded text-gray-400">v2.0.3</span>
      </div>
      
      <div className={`border-2 border-dashed rounded-2xl p-8 text-center transition-all duration-300 ${file ? 'border-nepalRed bg-nepalRed/5' : 'border-gray-600 hover:border-nepalBlue hover:bg-white/5'}`}>
        <input type="file" id="file" className="hidden" onChange={handleFileChange} />
        <label htmlFor="file" className="cursor-pointer flex flex-col items-center gap-4">
          <div className="w-16 h-16 rounded-full bg-white/10 flex items-center justify-center">
             {file ? <FileText size={32} className="text-gold" /> : <Upload size={32} className="text-gray-400" />}
          </div>
          <div>
            <span className="text-lg font-medium text-white block mb-1">{file ? file.name : "Drop file or click to upload"}</span>
            <span className="text-xs text-gray-400">{file ? `${(file.size / 1024).toFixed(2)} KB` : "Supports DOCX, JPG, PNG, PDF..."}</span>
          </div>
        </label>
      </div>

      <div className="flex gap-4 items-end">
        <div className="flex-1">
          <label className="block text-xs text-gray-400 mb-2 uppercase tracking-wide">Convert To</label>
          <select 
            value={targetFormat} 
            onChange={(e) => setTargetFormat(e.target.value)}
            className="w-full bg-black/40 border border-white/10 rounded-lg p-3 text-white focus:border-gold outline-none appearance-none"
          >
            {FILE_FORMATS.map(f => <option key={f} value={f}>{f}</option>)}
          </select>
        </div>
        
        {status === 'done' ? (
          <button 
            onClick={downloadFile} 
            className="flex-1 bg-green-600 text-white p-3 rounded-lg font-bold hover:shadow-lg hover:bg-green-500 transition-all flex items-center justify-center gap-2 animate-in fade-in"
          >
            <Download size={20} /> Download File
          </button>
        ) : (
          <button 
            onClick={convert} 
            disabled={!file || status === 'converting'}
            className="flex-1 bg-gradient-to-r from-nepalBlue to-blue-700 text-white p-3 rounded-lg font-bold disabled:opacity-50 hover:shadow-lg hover:shadow-blue-900/50 transition-all flex items-center justify-center gap-2"
          >
            {status === 'converting' ? <RefreshCw className="animate-spin" /> : <RefreshCw />}
            {status === 'converting' ? 'Processing...' : 'Convert'}
          </button>
        )}
      </div>

      {status === 'done' && (
        <div className="bg-green-500/20 border border-green-500/50 p-3 rounded-lg text-center text-green-300 text-sm animate-in fade-in slide-in-from-bottom-2">
          File ready! Click download to save your {targetFormat} file.
        </div>
      )}
    </div>
  );
};

const WorldClock = () => {
  const [searchTerm, setSearchTerm] = useState('');
  const [currentTime, setCurrentTime] = useState(new Date());

  // Get all supported timezones from the browser
  const timeZones = useMemo<string[]>(() => {
    try {
      // @ts-ignore: Property 'supportedValuesOf' does not exist on type 'typeof Intl' in some TS versions
      const supported = (Intl as any).supportedValuesOf('timeZone');
      if (supported && supported.length > 0) return supported;
      return FALLBACK_TIMEZONES;
    } catch (e) {
      return FALLBACK_TIMEZONES;
    }
  }, []);

  useEffect(() => {
    const timer = setInterval(() => setCurrentTime(new Date()), 1000);
    return () => clearInterval(timer);
  }, []);

  const filteredZones = useMemo(() => {
    const normalizedSearch = searchTerm.toLowerCase().replace(' ', '_');
    
    // Merge browser zones with fallback to ensure comprehensive list if searching
    const allZones = Array.from(new Set([...timeZones, ...FALLBACK_TIMEZONES]));
    
    return allZones.filter((zone) => 
      zone.toLowerCase().includes(normalizedSearch)
    ).slice(0, 50); // Limit results for performance
  }, [timeZones, searchTerm]);

  // Format timezone string for display (e.g., "America/New_York" -> "New York (America)")
  const formatZoneName = (zone: string) => {
    const parts = zone.split('/');
    const city = parts[parts.length - 1].replace(/_/g, ' ');
    const region = parts.length > 1 ? parts[0] : '';
    return { city, region };
  };

  return (
    <div className="flex flex-col gap-4 h-full">
      <h3 className="text-2xl font-bold text-gold mb-2">Global Timekeeper</h3>
      
      <div className="relative">
        <Search className="absolute left-3 top-1/2 -translate-y-1/2 text-gray-400" size={18} />
        <input 
          type="text" 
          placeholder="Search City (e.g. Kathmandu, New York)..." 
          value={searchTerm}
          onChange={(e) => setSearchTerm(e.target.value)}
          className="w-full bg-black/40 border border-white/10 rounded-full pl-10 pr-4 py-3 text-white focus:border-gold outline-none placeholder-gray-500"
        />
      </div>

      <div className="flex-1 overflow-y-auto pr-2 space-y-2 max-h-[300px]">
        {filteredZones.length > 0 ? filteredZones.map((zone) => {
          const { city, region } = formatZoneName(zone);
          return (
            <div key={zone} className="flex justify-between items-center bg-white/5 p-4 rounded-xl border border-white/5 hover:bg-white/10 transition-colors">
              <div>
                <span className="block font-bold text-lg">{city}</span>
                <span className="text-xs text-gray-400">{region}</span>
              </div>
              <div className="text-right">
                <span className="block font-mono text-xl text-gold font-bold">
                  {currentTime.toLocaleTimeString('en-US', { timeZone: zone, hour: '2-digit', minute: '2-digit' })}
                </span>
                <span className="text-xs text-gray-400">
                  {currentTime.toLocaleDateString('en-US', { timeZone: zone, weekday: 'short', month: 'short', day: 'numeric' })}
                </span>
              </div>
            </div>
          );
        }) : (
          <div className="text-center text-gray-500 mt-10">
             No matching locations found.<br/>Try searching for a continent or major city.
          </div>
        )}
      </div>
    </div>
  );
};

